"use client";

import { useEffect, useState } from "react";
import { supabase } from "../../lib/supabase";
import PageShell from "../../components/ui/PageShell";
import CmsSection from "../../components/ui/CmsSection";
import { uiTokens } from "../../lib/uiTokens";

type ModuleRow = {
  id: string;
  slug: string;
  title: string;
  order_index: number;
};

type LessonRow = {
  id: string;
  module_id: string;
  slug: string;
  title: string;
  order_index: number;
};

export default function ModulesBrowserPage() {
  const [modules, setModules] = useState<ModuleRow[]>([]);
  const [lessons, setLessons] = useState<LessonRow[]>([]);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    async function load() {
      const { data: modulesData, error: modulesError } = await supabase
        .from("modules")
        .select("id, slug, title, order_index")
        .order("order_index", { ascending: true });

      if (modulesError) {
        setError(modulesError.message);
        return;
      }

      const { data: lessonsData, error: lessonsError } = await supabase
        .from("lessons")
        .select("id, module_id, slug, title, order_index")
        .order("order_index", { ascending: true });

      if (lessonsError) {
        setError(lessonsError.message);
        return;
      }

      setModules(modulesData ?? []);
      setLessons(lessonsData ?? []);
    }

    load();
  }, []);

  return (
    <PageShell title="Modules Browser">
      {error && <p style={{ color: uiTokens.color.danger }}>{error}</p>}

      {modules.map((module) => {
        const moduleLessons = lessons.filter(
          (lesson) => lesson.module_id === module.id
        );

        return (
          <CmsSection
            key={module.id}
            title={
              <>
                {module.title}{" "}
                <span className="metaText">({module.slug})</span>
              </>
            }
          >
            {moduleLessons.length === 0 && (
              <p>No lessons in this module yet.</p>
            )}

            <ul style={{ paddingLeft: uiTokens.space.md, margin: 0 }}>
              {moduleLessons.map((lesson) => (
                <li key={lesson.id} style={{ marginBottom: uiTokens.space.xs }}>
                  {lesson.order_index}. {lesson.title}{" "}
                  <span className="metaText">({lesson.slug})</span>
                </li>
              ))}
            </ul>
          </CmsSection>
        );
      })}
    </PageShell>
  );
}
